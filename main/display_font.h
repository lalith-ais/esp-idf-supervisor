/*
 * display_font.h
 *
 * Pre-rendered bitmap glyphs from Roboto Condensed Bold.
 * Generated offline -- zero runtime font processing.
 *
 * TWO FONTS:
 *   TEXT font  16pt  -- full ASCII 32-126, cell height 20px (2 pages)
 *              used in blue zone (rows 16-63)
 *   CLOCK font 14pt  -- digits 0-9 and colon, cell height 16px (2 pages)
 *              used in yellow zone (rows 0-15)
 *
 * Glyph format: SSD1306 page format, column-major within each page.
 *   data[page * glyph_width + col]
 *   bit0 = topmost pixel of page, bit7 = bottommost.
 *
 * Each glyph has a variable width stored in the glyph_info table.
 * Rendering: blit column-by-column into the framebuffer zone.
 */

